public class Loops {
    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++) {
            System.out.println("Hello");
        }
        int i = 1;
        while (i <= 5) {
            System.out.println("Hello");
            i++;
        }
        int j = 1;
        do {
            System.out.println("Hello");
            j++;
        } while (j <= 5);
    }
}
